    /**
     * metodo para calcular as operações com porcentagem
     */
    private void calculaPorcentagem() {
        switch (sinal) {
            case '+':

                jtfTela.setText(String.valueOf(utils.somarPorcentagem(valor1, valor2)));
                jtfTelaAuxiliar.setText(utils.limpaTela());
                break;

            case '-':

                jtfTela.setText(String.valueOf(utils.subtrairPorcentagem(valor1, valor2)));
                jtfTelaAuxiliar.setText(utils.limpaTela());
                break;

            case 'X':

                jtfTela.setText(String.valueOf(utils.multiplicarPorcentagem(valor1, valor2)));
                jtfTelaAuxiliar.setText(utils.limpaTela());
                break;

            case '÷':

                jtfTela.setText(String.valueOf(utils.dividirPorcentagem(valor1, valor2)));
                jtfTelaAuxiliar.setText(utils.limpaTela());
                break;

            default:
                utils.limpaTela();
        }
    }

