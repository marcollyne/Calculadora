    private void jbtnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIgualActionPerformed
        // botão igual

        if (jtfTela.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Insira algum número para realizar a operação", "Informação", JOptionPane.ERROR_MESSAGE);
        } else {
            valor2 = Double.parseDouble(jtfTela.getText());

            switch (sinal) {
                case '+':
                    jtfTela.setText(String.valueOf(utils.somar(valor1, valor2)));
                    jtfTelaAuxiliar.setText(utils.limpaTela());
                    break;

                case '-':
                    jtfTela.setText(String.valueOf(utils.subtrair(valor1, valor2)));
                    jtfTelaAuxiliar.setText(utils.limpaTela());
                    break;

                case 'X':
                    jtfTela.setText(String.valueOf(utils.multiplicar(valor1, valor2)));
                    jtfTelaAuxiliar.setText(utils.limpaTela());
                    break;

                case '÷':
                    jtfTela.setText(String.valueOf(utils.dividir(valor1, valor2)));
                    jtfTelaAuxiliar.setText(utils.limpaTela());
                    break;

                case '√':
                    jtfTela.setText(String.valueOf(utils.raiz(valor1)));
                    jtfTelaAuxiliar.setText(utils.limpaTela());
                    break;

                default:
                    utils.limpaTela();

            }
        }

    }//GEN-LAST:event_jbtnIgualActionPerformed

